#include "libft.h"

int	ft_howmanydig(int n)
{
	long int size;

	size = 0;
	if (n < 0)
	{
		n = n * (-1);
		size++;
	}
	if (n == 0)
		size = 1;
	while (n > 0)
	{
		n = n / 10;
		size++;
	}
	return (size);
}
